char set_Ax[5],set_Ay[5],set_Az[5],set_Gx[5],set_Gy[5],set_Gz[5];   // Variables de impresion
int ang_x, ang_y, ang_z, ang_x_prev, ang_y_prev, ang_z_prev;
float accel_ang_x, accel_ang_y;

void read_MPU6050(){
	get_MPU6050();
	//------------------- Calcular giro con Acelerometro ----------------------------
	float accel_ang_x = atan(get_Ay/sqrt(pow(get_Ax,2) + pow(get_Az,2)))*(180.0/3.14);
	float accel_ang_y = atan(get_Ax/sqrt(pow(get_Ay,2) + pow(get_Az,2)))*(180.0/3.14);
	
	//------------------- Calcular giro con Filtro Complementario ----------------------------
	ang_x = 0.8*(ang_x_prev + (get_Gx/131)) + 0.2*(accel_ang_x);
	ang_y =-(0.5*(ang_y_prev + (get_Gy/131)) + 0.5*(accel_ang_y));
	ang_z = ang_z_prev + (get_Gz/131);
	ang_x_prev = ang_x;
	ang_y_prev = ang_y;
	ang_z_prev = ang_z;
	
	dtostrf(ang_x, 2, 0, set_Gx);
	dtostrf(ang_y, 2, 0, set_Gy);
	dtostrf(ang_z, 2, 0, set_Gz);
}
