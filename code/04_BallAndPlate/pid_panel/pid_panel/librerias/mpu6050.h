int get_Ax, get_Ay, get_Az, get_Gx, get_Gy, get_Gz, get_Temperature;            // Variables de registros
void mpu6050_init()										/* Gyro initialization function */
{
	_delay_ms(150);										/* Power up time >100ms */
	I2C_Start_Wait(0xD0);								/* Start with device write address */
	I2C_Write(SMPLRT_DIV);								/* Write to sample rate register */
	I2C_Write(0x07);									/* 1KHz sample rate */
	I2C_Stop();

	I2C_Start_Wait(0xD0);
	I2C_Write(PWR_MGMT_1);								/* Write to power management register */
	I2C_Write(0x01);									/* X axis gyroscope reference frequency */
	I2C_Stop();

	I2C_Start_Wait(0xD0);
	I2C_Write(CONFIG);									/* Write to Configuration register */
	I2C_Write(0x00);									/* Fs = 8KHz */
	I2C_Stop();

	I2C_Start_Wait(0xD0);
	I2C_Write(GYRO_CONFIG);								/* Write to Gyro configuration register */
	I2C_Write(0x18);									/* Full scale range +/- 2000 degree/C */
	I2C_Stop();

	I2C_Start_Wait(0xD0);
	I2C_Write(INT_ENABLE);								/* Write to interrupt enable register */
	I2C_Write(0x01);
	I2C_Stop();
}

void MPU_Start_Loc()
{
	I2C_Start_Wait(0xD0);								/* I2C start with device write address */
	I2C_Write(ACCEL_XOUT_H);							/* Write start location address from where to read */
	I2C_Repeated_Start(0xD1);							/* I2C start with device read address */
}

void get_MPU6050()
{
	MPU_Start_Loc();									/* Read Gyro values */
	get_Ax = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Ack());
	get_Ay = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Ack());
	get_Az = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Ack());
	get_Temperature = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Ack());
	get_Gx = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Ack());
	get_Gy = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Ack());
	get_Gz = (((int)I2C_Read_Ack()<<8) | (int)I2C_Read_Nack());
	I2C_Stop();
}

